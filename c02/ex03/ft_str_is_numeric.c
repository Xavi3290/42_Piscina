int	ft_str_is_numeric(char *str)
{
	int	num;
	int	i;

	i = 0;
	num = 1;
	while (str[i] != '\0')
	{
		if (str[i] > 47 && str[i] < 58)
			num = 1;
		else
			return (0);
		i++;
	}
	return (num);
}
