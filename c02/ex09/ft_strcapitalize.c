char	*ft_strcapitalize(char *str)
{
	int		i;
	char	c;
	char	p;

	i = 0;
	c = str[0];
	p = '\0';
	while (c != '\0')
	{
		if ((p < 48 || p > 57) && (p < 'a' || p > 'z') && (p < 'A' || p > 'Z'))
		{
			if (c >= 'a' && c <= 'z')
				str[i] -= 32;
		}
		else if (c >= 'A' && c <= 'Z')
			str[i] += 32;
		p = str[i];
		i++;
		c = str[i];
	}
	return (str);
}
