int	ft_str_is_uppercase(char *str)
{
	int	up;
	int	i;

	i = 0;
	up = 1;
	while (str[i] != '\0')
	{
		if (str[i] > 64 && str[i] < 91)
			up = 1;
		else
			return (0);
		i++;
	}
	return (up);
}
